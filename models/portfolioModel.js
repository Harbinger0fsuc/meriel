const mongoose = require("mongoose");

const portfolioSchema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    subtitle: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    thumbs: [String],
  },
  { timestamps: true }
);

module.exports = mongoose.model("Portfolio", portfolioSchema);
